package model;

public class Player {

    private int stamina;
    private int staminaMax;
    private int numeriTurni;
    private int numeroDemoniDaBattere;

    public int getStamina() {
        return stamina;
    }

    public void setStamina(int stamina) {
        this.stamina = stamina;
    }

    public int getStaminaMax() {
        return staminaMax;
    }

    public void setStaminaMax(int staminaMax) {
        this.staminaMax = staminaMax;
    }

    public int getNumeriTurni() {
        return numeriTurni;
    }

    public void setNumeriTurni(int numeriTurni) {
        this.numeriTurni = numeriTurni;
    }

    public int getNumeroDemoniDaBattere() {
        return numeroDemoniDaBattere;
    }

    public void setNumeroDemoniDaBattere(int numeroDemoniDaBattere) {
        this.numeroDemoniDaBattere = numeroDemoniDaBattere;
    }
}
